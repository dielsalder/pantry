import { useAtomValue } from "jotai";
import Head from "next/head";
import Link from "next/link";
import { userAtom } from "~/user";
import { AppShell, Box, Loader, Space, Stack, Title } from "@mantine/core";

import { api } from "~/utils/api";

export default function Home() {
  const userId = useAtomValue(userAtom);
  const { data, isLoading } = api.user.pantry.useQuery(userId);

  return (
    <>
      <Head>
        <title>pantry</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <AppShell header={{ height: "60" }} p="md">
        <AppShell.Header p="md">
          <Title order={3}>jude food</Title>
        </AppShell.Header>
        <AppShell.Main>
          <Title order={1} mb="md">
            Pantry
          </Title>
          {isLoading ? (
            <Loader />
          ) : (
            <Stack>{data?.map((item) => <Box>{item.name}</Box>)}</Stack>
          )}
        </AppShell.Main>
      </AppShell>
    </>
  );
}
